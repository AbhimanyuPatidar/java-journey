// Code to demonstrate printing Hello World! on console

package com.java.journey;

public class HelloWorld {
    public static void main(String[] args) {
        System.out.println("Hello World!");
    }
}

/*
    Output: 
    Hello World!

    Explanation: 
    In the above code, we have created a class named HelloWorld and a method named main. The main method is the entry point of the program. The System.out.println() method is used to print the text Hello World! on the console. 
    The System class is a final class in the java.lang package. It provides several methods to interact with the system. The out is a static member of the System class and is of type PrintStream. It is used to print the output on the console. The println() method is used to print the text on the console and move the cursor to the next line. 
    You can also use the print() method to print the text on the console without moving the cursor to the next line. 
    System.out.print("Hello World!");
*/